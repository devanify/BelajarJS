/*
Perulangan : Melakukan hal yang sama sebanyak data atau kondisi
Tipe - tipe perulangan ;
- for
- while
- for ... of
- for ... in
 */

// Contoh 1 Perulangan
for (let i = 1 ; i <= 10 ; i++) {
    console.log(`ini nomor ${i}`);
}

//contoh 2 perulangan
for (let x = 0; x <= 10; x += 2){
    console.log (x);
}